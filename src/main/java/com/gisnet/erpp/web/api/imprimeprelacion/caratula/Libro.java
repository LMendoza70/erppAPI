package com.gisnet.erpp.web.api.imprimeprelacion.caratula;

public class Libro {
	private String procedeDeFolio;
	private String libro;
	private String bis;
	private String seccion;
	private String oficina;
	private String tipoDocumento;
	private String numero;
	public String getProcedeDeFolio() {
		return procedeDeFolio;
	}
	public void setProcedeDeFolio(String procedeDeFolio) {
		this.procedeDeFolio = procedeDeFolio;
	}
	public String getLibro() {
		return libro;
	}
	public void setLibro(String libro) {
		this.libro = libro;
	}
	public String getBis() {
		return bis;
	}
	public void setBis(String bis) {
		this.bis = bis;
	}
	public String getSeccion() {
		return seccion;
	}
	public void setSeccion(String seccion) {
		this.seccion = seccion;
	}
	public String getOficina() {
		return oficina;
	}
	public void setOficina(String oficina) {
		this.oficina = oficina;
	}
	public String getTipoDocumento() {
		return tipoDocumento;
	}
	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}

}
