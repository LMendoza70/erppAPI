public class CumpleAvisoDTO {
	private Boolean cumple;
	private Boolean preventivo;
	private Boolean cautelar;
	public Boolean getCumple() {
		return cumple;
	}
	public void setCumple(Boolean cumple) {
		this.cumple = cumple;
	}
	public Boolean getPreventivo() {
		return preventivo;
	}
	public void setPreventivo(Boolean preventivo) {
		this.preventivo = preventivo;
	}
	public Boolean getCautelar() {
		return cautelar;
	}
	public void setCautelar(Boolean cautelar) {
		this.cautelar = cautelar;
	}

}
