package com.gisnet.erpp.vo.fichaRegistral;

public class PaseFichaVO {
	private String 	superfice;
	private Integer noFolio;
    private String 	unidadMedida;
    private Integer noFolioSegregado;
    private String 	claveCatastralSegregado;	
	private String 	cuentaCatastralSegregado;
	
	public String getSuperfice() {
		return superfice;
	}
	public void setSuperfice(String superfice) {
		this.superfice = superfice;
	}
	public Integer getNoFolio() {
		return noFolio;
	}
	public void setNoFolio(Integer noFolio) {
		this.noFolio = noFolio;
	}
	public String getUnidadMedida() {
		return unidadMedida;
	}
	public void setUnidadMedida(String unidadMedida) {
		this.unidadMedida = unidadMedida;
	}
	public Integer getNoFolioSegregado() {
		return noFolioSegregado;
	}
	public void setNoFolioSegregado(Integer noFolioSegregado) {
		this.noFolioSegregado = noFolioSegregado;
	}
	public String getClaveCatastralSegregado() {
		return claveCatastralSegregado;
	}
	public void setClaveCatastralSegregado(String claveCatastralSegregado) {
		this.claveCatastralSegregado = claveCatastralSegregado;
	}
	public String getCuentaCatastralSegregado() {
		return cuentaCatastralSegregado;
	}
	public void setCuentaCatastralSegregado(String cuentaCatastralSegregado) {
		this.cuentaCatastralSegregado = cuentaCatastralSegregado;
	}

}
