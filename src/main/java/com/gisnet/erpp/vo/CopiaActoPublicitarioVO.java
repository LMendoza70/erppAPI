package com.gisnet.erpp.vo;

public class CopiaActoPublicitarioVO {
	public String entradaActual; 
	public String entrada_historica;
	public String anio;
	public String subindice;
	public String numero_acto_publicitario;
	public String nombre_tipo_acto;
	public String solicitante;
	public String folioReal;
	
	public String getEntradaActual() {
		return entradaActual;
	}
	public void setEntradaActual(String entradaActual) {
		this.entradaActual = entradaActual;
	}
	public String getEntrada_historica() {
		return entrada_historica;
	}
	public void setEntrada_historica(String entrada_historica) {
		this.entrada_historica = entrada_historica;
	}
	public String getAnio() {
		return anio;
	}
	public void setAnio(String anio) {
		this.anio = anio;
	}
	public String getSubindice() {
		return subindice;
	}
	public void setSubindice(String subindice) {
		this.subindice = subindice;
	}
	public String getNumero_acto_publicitario() {
		return numero_acto_publicitario;
	}
	public void setNumero_acto_publicitario(String numero_acto_publicitario) {
		this.numero_acto_publicitario = numero_acto_publicitario;
	}
	public String getNombre_tipo_acto() {
		return nombre_tipo_acto;
	}
	public void setNombre_tipo_acto(String nombre_tipo_acto) {
		this.nombre_tipo_acto = nombre_tipo_acto;
	}
	public String getSolicitante() {
		return solicitante;
	}
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	public String getFolioReal() {
		return folioReal;
	}
	public void setFolioReal(String folioReal) {
		this.folioReal = folioReal;
	}
	
}
