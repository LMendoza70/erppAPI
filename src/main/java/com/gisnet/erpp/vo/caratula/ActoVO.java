package com.gisnet.erpp.vo.caratula;

public class ActoVO {
	private long clasifActoId;
	private String nombre;
	private int vigentes;
	private int extintos;
	
	public long getClasifActoId() {
		return clasifActoId;
	}
	public void setClasifActoId(long clasifActoId) {
		this.clasifActoId = clasifActoId;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getVigentes() {
		return vigentes;
	}
	public void setVigentes(int vigentes) {
		this.vigentes = vigentes;
	}
	public int getExtintos() {
		return extintos;
	}
	public void setExtintos(int extintos) {
		this.extintos = extintos;
	}
	
	

}
