package com.gisnet.erpp.vo.caratula;

public class CaratulaImpresionVO {
	private String entrada;
	private String noFolio;
	private String numeroFolioReal;
	private String auxiliar;
	private String anio;
	private String subindice;
	private String oficina;
	
	public String getAnio() {
		return anio;
	}
	public void setAnio(String anio) {
		this.anio = anio;
	}
	public String getSubindice() {
		return subindice;
	}
	public void setSubindice(String subindice) {
		this.subindice = subindice;
	}
	public String getEntrada() {
		return entrada;
	}
	public void setEntrada(String entrada) {
		this.entrada = entrada;
	}
	public String getNoFolio() {
		return noFolio;
	}
	public void setNoFolio(String noFolio) {
		this.noFolio = noFolio;
	}
	public String getNumeroFolioReal() {
		return numeroFolioReal;
	}
	public void setNumeroFolioReal(String numeroFolioReal) {
		this.numeroFolioReal = numeroFolioReal;
	}
	public String getAuxiliar() {
		return auxiliar;
	}
	public void setAuxiliar(String auxiliar) {
		this.auxiliar = auxiliar;
	}
	public String getOficina() {
		return oficina;
	}
	public void setOficina(String oficina) {
		this.oficina = oficina;
	}
	
}
