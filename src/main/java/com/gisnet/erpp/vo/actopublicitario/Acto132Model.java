package com.gisnet.erpp.vo.actopublicitario;

public class Acto132Model {

	String testador;
	String solicitante;

	String datosDeRevocacion;
	String obs;
	String tipoDeDocumento;
	
	public String getTestador() {
		return testador;
	}
	public void setTestador(String testador) {
		this.testador = testador;
	}
	public String getSolicitante() {
		return solicitante;
	}
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	public String getDatosDeRevocacion() {
		return datosDeRevocacion;
	}
	public void setDatosDeRevocacion(String datosDeRevocacion) {
		this.datosDeRevocacion = datosDeRevocacion;
	}
	public String getObs() {
		return obs;
	}
	public void setObs(String obs) {
		this.obs = obs;
	}
	public String getTipoDeDocumento() {
		return tipoDeDocumento;
	}
	public void setTipoDeDocumento(String tipoDeDocumento) {
		this.tipoDeDocumento = tipoDeDocumento;
	}
		
}
